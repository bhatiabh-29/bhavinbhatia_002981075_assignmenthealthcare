    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        // Get patient Id
        int existingPatientId = Integer.parseInt(txtPatientId.getText());
        this.patientId = existingPatientId;
        Patient searchedPatient = this.patientDirectory
                .getPatientById(existingPatientId);
        EncounterHistory encounterHistory = searchedPatient.getEncounterHistory();
        this.encounterHistory = encounterHistory;
        populateEncounterHistory(encounterHistory);

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSelectEncounterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectEncounterActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEncounterHistory.getSelectedRow();
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog( this, "Please select the row first");
        } 
        DefaultTableModel tableModel = (DefaultTableModel) tblEncounterHistory.getModel();
        
        
       tableModel.getValueAt(selectedRowIndex, 0);
      
        Encounter encounter = this.encounterHistory.getEncounters().get(selectedRowIndex);
        this.newVitalSigns = encounter.getVitalSigns();

    
        
    txtpulse.setText(String.valueOf(newVitalSigns.getPulseRate()));
    txtTemp.setText(String.valueOf(newVitalSigns.getTemperature()));
    txtBloodPressure.setText(String.valueOf(newVitalSigns.getBloodPressure()));
    txtSymptoms.setText(newVitalSigns.getSymptoms());
    txtAlergies.setText(newVitalSigns.getAlergies());
    txtMedication.setText(newVitalSigns.getMedication());
    }//GEN-LAST:event_btnSelectEncounterActionPerformed


