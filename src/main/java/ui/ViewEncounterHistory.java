    public void populateEncounterHistory(EncounterHistory encounterHistory){
  
     DefaultTableModel model = (DefaultTableModel) tblEncounterHistory.getModel();
        model.setRowCount(0);
        
        for (Encounter encounter : encounterHistory.getEncounters()){
            Object [] row = new Object[3];
            row[0]= encounter.getDate();          
            model.addRow(row);
        }

    
    }
